CREATE OR REPLACE VIEW "public"."e10_obs_format_product_perf" AS
 SELECT "p"."full_name" AS "product_name",
    "p"."product_format",
    "b"."format" AS "break_format",
    "b"."spot_type",
    "count"(DISTINCT "b"."id") AS "breaks_observed",
    "round"("avg"("be"."avg_slot_price"), 2) AS "avg_slot_price",
    "round"("avg"("be"."gross_sold"), 2) AS "avg_gross_per_break",
    "round"("avg"("be"."margin_post_fee_pct"), 1) AS "avg_margin_post_fee_pct",
    "round"("avg"("st"."peak_viewers"), 0) AS "avg_peak_viewers"
   FROM ((("public"."e10_obs_breaks" "b"
     JOIN "public"."e10_obs_break_economics" "be" ON (("be"."break_id" = "b"."id")))
     JOIN "public"."e10_obs_streams" "st" ON (("st"."id" = "b"."stream_id")))
     LEFT JOIN "public"."e10_obs_products" "p" ON (("p"."id" = "b"."product_id")))
  GROUP BY "p"."full_name", "p"."product_format", "b"."format", "b"."spot_type";


