CREATE OR REPLACE VIEW "public"."e10_obs_slot_economics" AS
 WITH "cfg" AS (
         SELECT (( SELECT "e10_obs_config"."num_value"
                   FROM "public"."e10_obs_config"
                  WHERE ("e10_obs_config"."key" = 'whatnot_commission_pct'::"text")) / 100.0) AS "comm",
            (( SELECT "e10_obs_config"."num_value"
                   FROM "public"."e10_obs_config"
                  WHERE ("e10_obs_config"."key" = 'whatnot_processing_pct'::"text")) / 100.0) AS "proc_pct",
            ( SELECT "e10_obs_config"."num_value"
                   FROM "public"."e10_obs_config"
                  WHERE ("e10_obs_config"."key" = 'whatnot_processing_flat'::"text")) AS "proc_flat"
        )
 SELECT "s"."id" AS "slot_id",
    "s"."break_id",
    "s"."slot_number",
    "s"."label",
    "s"."price",
    "s"."buyer_handle",
    "s"."sold_at",
    "s"."viewers_at_sale",
    "round"((COALESCE("s"."price", (0)::numeric) * "cfg"."comm"), 2) AS "whatnot_commission",
    "round"(((COALESCE("s"."price", (0)::numeric) * "cfg"."proc_pct") +
        CASE
            WHEN ("s"."price" > (0)::numeric) THEN "cfg"."proc_flat"
            ELSE (0)::numeric
        END), 2) AS "whatnot_processing",
    "round"(((COALESCE("s"."price", (0)::numeric) * ("cfg"."comm" + "cfg"."proc_pct")) +
        CASE
            WHEN ("s"."price" > (0)::numeric) THEN "cfg"."proc_flat"
            ELSE (0)::numeric
        END), 2) AS "whatnot_fee_total",
    "round"((COALESCE("s"."price", (0)::numeric) - ((COALESCE("s"."price", (0)::numeric) * ("cfg"."comm" + "cfg"."proc_pct")) +
        CASE
            WHEN ("s"."price" > (0)::numeric) THEN "cfg"."proc_flat"
            ELSE (0)::numeric
        END)), 2) AS "net_after_fees"
   FROM ("public"."e10_obs_slots" "s"
     CROSS JOIN "cfg")
  WHERE ("s"."state" = 'sold'::"text");


ALTER VIEW "public"."e10_obs_slot_economics" OWNER TO "postgres";


