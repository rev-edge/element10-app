CREATE OR REPLACE FUNCTION "public"."e10_slot_pred"("conds" "jsonb") RETURNS "text"
    LANGUAGE "plpgsql" IMMUTABLE
    SET "search_path" TO 'public'
    AS $$
declare c jsonb; parts text[] := '{}'; field text; val text; vid text; p text;
begin
  if conds is null or jsonb_typeof(conds) <> 'array' or jsonb_array_length(conds)=0 then return null; end if;
  for c in select * from jsonb_array_elements(conds) loop
    field := c->>'field'; val := nullif(btrim(coalesce(c->>'value','')),''); vid := nullif(btrim(coalesce(c->>'valueId','')),'');
    p := null;
    if field='team' then
      if vid is not null then p := 'team_id = '||quote_literal(vid); elsif val is not null then p := 'team ilike '||quote_literal(val); end if;
    elsif field='player' then
      if vid is not null then p := 'player_id = '||quote_literal(vid); elsif val is not null then p := 'name ilike '||quote_literal(val); end if;
    elsif field='set' then if val is not null then p := 'set_name ilike '||quote_literal(val); end if;
    elsif field='parallel' then if val is not null then p := 'parallel ilike '||quote_literal(val); end if;
    elsif field in ('type','card_type') then if val is not null then p := 'card_type ilike '||quote_literal(val); end if;
    end if;
    if p is not null then parts := parts || p; end if;
  end loop;
  if array_length(parts,1) is null then return null; end if;
  return array_to_string(parts,' and ');
end $$;


