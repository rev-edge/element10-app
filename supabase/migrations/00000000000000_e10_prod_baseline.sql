CREATE OR REPLACE FUNCTION "public"."e10_slot_partition"("p_checklist" "uuid", "p_slots" "jsonb") RETURNS "jsonb"
    LANGUAGE "plpgsql"
    SET "search_path" TO 'public'
    AS $$
declare
  n int := coalesce(jsonb_array_length(p_slots),0);
  i int; slot jsonb; incp text; excp text; whr text;
  total bigint; cnt bigint; val numeric; slots_out jsonb := '[]'::jsonb;
begin
  create temp table _slotmap(card_id uuid, slot_i int, slot_name text) on commit drop;
  total := (select count(*) from public.e10_cards where checklist_id=p_checklist);
  -- pass 1: rule + explicit-id slots (skip remainder)
  for i in 0..n-1 loop
    slot := p_slots->i;
    if coalesce((slot->>'remainder')::boolean,false) then continue; end if;
    if jsonb_typeof(slot->'cardIds')='array' and jsonb_array_length(slot->'cardIds')>0 then
      insert into _slotmap select (e)::uuid, i, coalesce(slot->>'name','') from jsonb_array_elements_text(slot->'cardIds') e;
    end if;
    incp := public.e10_slot_pred(slot->'include');
    if incp is not null then
      whr := 'checklist_id = '||quote_literal(p_checklist)||' and ('||incp||')';
      excp := public.e10_slot_pred(slot->'exclude');
      if excp is not null then whr := whr||' and not ('||excp||')'; end if;
      execute 'insert into _slotmap select id, '||i||', '||quote_literal(coalesce(slot->>'name',''))||' from public.e10_cards where '||whr;
    end if;
  end loop;
  -- pass 2: remainder slots claim the unassigned cards
  for i in 0..n-1 loop
    slot := p_slots->i;
    if coalesce((slot->>'remainder')::boolean,false) then
      execute 'insert into _slotmap select id, '||i||', '||quote_literal(coalesce(slot->>'name','Field'))||
        ' from public.e10_cards c where c.checklist_id='||quote_literal(p_checklist)||
        ' and not exists(select 1 from _slotmap m where m.card_id=c.id)';
    end if;
  end loop;
  for i in 0..n-1 loop
    select count(*), coalesce(sum(c.value),0) into cnt, val
      from (select distinct card_id from _slotmap where slot_i=i) m join public.e10_cards c on c.id=m.card_id;
    slots_out := slots_out || jsonb_build_object('i',i,'name',(p_slots->i->>'name'),'matched',coalesce(cnt,0),'value',coalesce(val,0),'remainder',coalesce((p_slots->i->>'remainder')::boolean,false));
  end loop;
  return jsonb_build_object(
    'total', total,
    'slots', slots_out,
    'cover', jsonb_build_object(
      'unassigned', (select count(*) from public.e10_cards c where c.checklist_id=p_checklist and not exists(select 1 from _slotmap m where m.card_id=c.id)),
      'sample', coalesce((select jsonb_agg(x.name) from (select c.name from public.e10_cards c where c.checklist_id=p_checklist and not exists(select 1 from _slotmap m where m.card_id=c.id) limit 8) x),'[]'::jsonb)),
    'disjoint', jsonb_build_object(
      'doubled', (select count(*) from (select card_id from _slotmap group by card_id having count(distinct slot_i)>1) d),
      'sample', coalesce((select jsonb_agg(jsonb_build_object('name',y.name,'slots',y.slots)) from (
          select c.name, array_agg(distinct m.slot_name) as slots from _slotmap m join public.e10_cards c on c.id=m.card_id
          group by c.id, c.name having count(distinct m.slot_i)>1 limit 8) y),'[]'::jsonb))
  );
end $$;


