CREATE OR REPLACE FUNCTION "public"."e10_checklist_facet"("p_checklist" "uuid", "p_dim" "text") RETURNS "jsonb"
    LANGUAGE "plpgsql" STABLE
    SET "search_path" TO 'public'
    AS $$
declare out jsonb;
begin
  if p_dim = 'team' then
    select coalesce(jsonb_agg(jsonb_build_object('label',t.team,'id',t.team_id,'cards',t.cards,'value',t.value)
             order by t.value desc, t.cards desc, t.team),'[]'::jsonb)
      into out
      from (select team, team_id, count(*) as cards, coalesce(sum(value),0) as value
              from public.e10_cards
             where checklist_id = p_checklist and coalesce(btrim(team),'') <> ''
             group by team, team_id) t;
  elsif p_dim = 'player' then
    select coalesce(jsonb_agg(jsonb_build_object('label',t.name,'id',t.player_id,'cards',t.cards,'value',t.value)
             order by t.value desc, t.cards desc, t.name),'[]'::jsonb)
      into out
      from (select name, player_id, count(*) as cards, coalesce(sum(value),0) as value
              from public.e10_cards
             where checklist_id = p_checklist and coalesce(btrim(name),'') <> ''
             group by name, player_id) t;
  else
    out := '[]'::jsonb;
  end if;
  return out;
end
$$;


