CREATE OR REPLACE VIEW "public"."e10_obs_break_economics" AS
 SELECT "b"."id" AS "break_id",
    "b"."stream_id",
    "ch"."handle" AS "channel",
    "ch"."family",
    "st"."title" AS "stream_title",
    "st"."sport",
    "b"."title" AS "break_title",
    "b"."format",
    "b"."spot_type",
    "b"."transaction_type",
    "b"."total_spots",
    "b"."boxes_per_break",
    "b"."product_id",
    "p"."full_name" AS "product_name",
    "p"."product_format",
    "b"."incentives",
    "count"("se"."slot_id") AS "slots_sold",
    "round"("avg"("se"."price"), 2) AS "avg_slot_price",
    "round"("sum"("se"."price"), 2) AS "gross_sold",
    "round"("sum"("se"."whatnot_fee_total"), 2) AS "whatnot_fees",
    "round"("sum"("se"."net_after_fees"), 2) AS "net_sold",
    "b"."cost_basis",
    "round"(("sum"("se"."price") - COALESCE("b"."cost_basis", (0)::numeric)), 2) AS "margin_pre_fee",
    "round"(("sum"("se"."net_after_fees") - COALESCE("b"."cost_basis", (0)::numeric)), 2) AS "margin_post_fee",
        CASE
            WHEN (COALESCE("b"."cost_basis", (0)::numeric) > (0)::numeric) THEN "round"(((("sum"("se"."net_after_fees") - "b"."cost_basis") / "b"."cost_basis") * (100)::numeric), 1)
            ELSE NULL::numeric
        END AS "margin_post_fee_pct",
        CASE
            WHEN ("b"."total_spots" > 0) THEN "round"(("sum"("se"."price") / ("b"."total_spots")::numeric), 2)
            ELSE NULL::numeric
        END AS "revenue_per_spot_offered",
    "st"."peak_viewers"
   FROM (((("public"."e10_obs_breaks" "b"
     JOIN "public"."e10_obs_streams" "st" ON (("st"."id" = "b"."stream_id")))
     JOIN "public"."e10_obs_channels" "ch" ON (("ch"."id" = "st"."channel_id")))
     LEFT JOIN "public"."e10_obs_products" "p" ON (("p"."id" = "b"."product_id")))
     LEFT JOIN "public"."e10_obs_slot_economics" "se" ON (("se"."break_id" = "b"."id")))
  GROUP BY "b"."id", "ch"."handle", "ch"."family", "st"."title", "st"."sport", "st"."peak_viewers", "p"."full_name", "p"."product_format";



