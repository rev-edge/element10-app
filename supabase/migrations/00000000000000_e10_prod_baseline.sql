            COALESCE("sum"("e10_inventory_movements"."reserved_delta"), (0)::numeric) AS "led_reserved"
           FROM "public"."e10_inventory_movements"
          WHERE ("e10_inventory_movements"."workspace_id" = 'shared'::"text")
          GROUP BY "e10_inventory_movements"."item_id"
        )
