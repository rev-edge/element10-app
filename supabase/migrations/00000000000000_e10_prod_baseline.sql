  i int; slot jsonb; incp text; excp text; whr text; lim int := least(greatest(coalesce(p_limit,60),1),300);
  cards jsonb; cnt bigint; out jsonb := '[]'::jsonb;
begin
  for i in 0..n-1 loop
    slot := p_slots->i;
    if coalesce((slot->>'remainder')::boolean,false) then
      out := out || jsonb_build_object('i',i,'count',null,'cards','[]'::jsonb,'remainder',true); continue;
    end if;
    incp := public.e10_slot_pred(slot->'include');
    if incp is null then
      out := out || jsonb_build_object('i',i,'count',0,'cards','[]'::jsonb); continue;
    end if;
    whr := 'checklist_id = '||quote_literal(p_checklist)||' and ('||incp||')';
    excp := public.e10_slot_pred(slot->'exclude');
    if excp is not null then whr := whr||' and not ('||excp||')'; end if;
    execute 'select count(*) from public.e10_cards where '||whr into cnt;
    execute 'select coalesce(jsonb_agg(x),''[]''::jsonb) from '
          ||'(select jsonb_build_object(''name'',name,''num'',num,''value'',value) x '
          ||'from public.e10_cards where '||whr||' order by value desc nulls last, name limit '||lim||') t'
      into cards;
    out := out || jsonb_build_object('i',i,'count',coalesce(cnt,0),'cards',coalesce(cards,'[]'::jsonb));
  end loop;
  return out;
end $$;


