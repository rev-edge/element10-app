CREATE OR REPLACE FUNCTION "public"."e10_assign_role"("p_user" "uuid", "p_role" "text") RETURNS "void"
    LANGUAGE "plpgsql" SECURITY DEFINER
    SET "search_path" TO 'public'
    AS $$
begin
  if not public.e10_is_admin() then raise exception 'Admin only' using errcode='42501'; end if;
  if coalesce(btrim(p_role),'')='' then raise exception 'Role required'; end if;
  perform 1 from public.e10_members where role='admin' for update;  -- serialize last-admin checks
  if p_role <> 'admin'
     and exists(select 1 from public.e10_members where user_id=p_user and role='admin')
     and (select count(*) from public.e10_members where role='admin') <= 1 then
     raise exception 'Cannot demote the last admin';
  end if;
  update public.e10_members set role=btrim(p_role) where user_id=p_user;
  if not found then raise exception 'No such member'; end if;
end; $$;


